    public ClientSearch3(StateofAccount parent,String s) {
        initComponents();
        centerFrame();
        this.parent = parent;
          this.s = s;
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        try{
             model.setRowCount(0);
             Class.forName("com.mysql.cj.jdbc.Driver");
              Connection con = DriverManager.getConnection("jdbc:mysql://localhost:                              3306/trainingproject","root","");
              Statement st = con.createStatement();
        ResultSet rs = st.executeQuery ("SELECT * FROM customers WHERE ID LIKE '%" + s +  "%' or Name LIKE '%" + s + "%'");
              while(rs.next())
              {
                  String id1 = rs.getString("ID");
                  String name = rs.getString("Name");
                model.addRow(
                new Object[] {id1,name});
              }
              st.close();
              con.close();
              parent.getSelectedType();
//                parent.jRadioButton1.isSelected();
                        
        }
         catch(Exception e)
          {
              System.out.println(e);
          }
    }

